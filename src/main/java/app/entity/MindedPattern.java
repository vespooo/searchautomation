package app.entity;

public class MindedPattern {
    private String startingWith;
    private String endingWith;

    public String getStartingWith() {
        return startingWith;
    }

    public void setStartingWith(String startingWith) {
        this.startingWith = startingWith;
    }

    public String getEndingWith() {
        return endingWith;
    }

    public void setEndingWith(String endingWith) {
        this.endingWith = endingWith;
    }
}
